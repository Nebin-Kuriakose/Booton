// Lightweight CSV parser (handles quoted fields and commas inside quotes)
function parseCSV(text) {
    const rows = [];
    const src = text.replace(/\r\n/g, '\n').replace(/\r/g, '\n');
    let field = '';
    let inQuotes = false;
    let row = [];

    for (let i = 0; i < src.length; i++) {
        const c = src[i];
        if (inQuotes) {
            if (c === '"') {
                if (src[i + 1] === '"') { // escaped quote
                    field += '"';
                    i++;
                } else {
                    inQuotes = false;
                }
            } else {
                field += c;
            }
        } else {
            if (c === '"') {
                inQuotes = true;
            } else if (c === ',') {
                row.push(field);
                field = '';
            } else if (c === '\n') {
                row.push(field);
                field = '';
                // finalize row
                if (row.length > 0 && row.some(v => v !== '')) {
                    rows.push(row);
                }
                row = [];
            } else {
                field += c;
            }
        }
    }
    // push last field/row if any
    if (field.length > 0 || row.length > 0) {
        row.push(field);
        rows.push(row);
    }

    return rows;
}

